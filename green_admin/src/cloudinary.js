// Cloudinary configuration
const CLOUDINARY_CLOUD_NAME = 'dfuon2cd9';
const CLOUDINARY_UPLOAD_PRESET = 'green_tips'; // Cần tạo preset này trong Cloudinary

export const uploadToCloudinary = async (file) => {
    const formData = new FormData();
    formData.append('file', file);
    formData.append('upload_preset', CLOUDINARY_UPLOAD_PRESET);
    formData.append('folder', 'green_recycle/tips');

    try {
        const response = await fetch(
            `https://api.cloudinary.com/v1_1/${CLOUDINARY_CLOUD_NAME}/image/upload`,
            {
                method: 'POST',
                body: formData,
            }
        );

        if (!response.ok) {
            throw new Error('Upload failed');
        }

        const data = await response.json();
        return data.secure_url;
    } catch (error) {
        console.error('Error uploading to Cloudinary:', error);
        throw error;
    }
};

export { CLOUDINARY_CLOUD_NAME, CLOUDINARY_UPLOAD_PRESET };
